using UnityEngine;

public class CharacterVisuals : MonoBehaviour {

	/* --------------------------------------------------------------------------------------------------------------------------------------------------------- //
		Declaration
	// --------------------------------------------------------------------------------------------------------------------------------------------------------- */

	private Animator an;

	// ---------------------------------------------------------------------------- //
	


	/* --------------------------------------------------------------------------------------------------------------------------------------------------------- //
		Initialisation
	// --------------------------------------------------------------------------------------------------------------------------------------------------------- */

	private void Awake() {

		ComponentGrab();

	}

	private void Start() {



	}

	// ---------------------------------------------------------------------------- */

	private void ComponentGrab() {

		an = GetComponentInChildren<Animator>();

	}

	/* --------------------------------------------------------------------------------------------------------------------------------------------------------- //
		Visual Calls
	// --------------------------------------------------------------------------------------------------------------------------------------------------------- */

	public void ShowMovement(float move) {

		an.SetFloat("movementSpeed", move);

	}

	// ---------------------------------------------------------------------------- */





}